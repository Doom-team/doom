#include "libft.h"

char	*ft_strlwr(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (ft_isalpha(s[i]))
			s[i] = ft_tolower(s[i]);
		i++;
	}
	return (s);
}
