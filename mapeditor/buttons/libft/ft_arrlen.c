#include "libft.h"

size_t		ft_arrlen(char **arr)
{
	size_t	i;

	i = 0;
	if (arr)
	{
		i = 1;
		while (*arr)
		{
			i++;
			arr++;
		}
		return (i - 1);
	}
	return (i);
}
